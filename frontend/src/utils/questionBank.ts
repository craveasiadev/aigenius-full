export function getRandomQuestion(pageIndex: number, seed?: string): InteractiveQuestionData {
  const determinedIndex = seed
    ? Math.abs(hashCode(seed + pageIndex)) % questionBank.length
    : (pageIndex * 3 + 7) % questionBank.length;

  return questionBank[determinedIndex];
}

function hashCode(str: string): number {
  let hash = 0;
  for (let i = 0; i < str.length; i++) {
    const char = str.charCodeAt(i);
    hash = ((hash << 5) - hash) + char;
    hash = hash & hash;
  }
  return hash;
}

export function getRandomQuestions(count: number, excludeIds: string[] = []): InteractiveQuestionData[] {
  const availableQuestions = questionBank.filter(q => !excludeIds.includes(q.id));
  const shuffled = [...availableQuestions].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}
